/** Replace {key} placeholders in a template string. */
export function tReplace(
  template: string,
  params: Record<string, string | number>
): string {
  return Object.entries(params).reduce(
    (s, [k, v]) => s.replace(`{${k}}`, String(v)),
    template
  );
}

